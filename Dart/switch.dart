//Decisões Limitadas (Switch) parte 1

void main() {
  String linguagem = "Dart";
  
  switch(linguagem){
    case "Java":
      print("Java");
      break;
      case "C#":
      print("C#");
      break;
      case "C++":
      print("C++");
      break;
      case "Dart":
      print("Dart");
      break;
      case "HTML":
      print("HTML");
      break;
    default:
      print("Escolha outra opção");
  }
}
